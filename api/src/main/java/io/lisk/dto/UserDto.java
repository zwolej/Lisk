package io.lisk.dto;

public class UserDto {

    private String id;
    private String username;
    private String password;
    private String email;

    public String getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public String getEmail() {
        return email;
    }
}
